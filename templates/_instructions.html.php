<h1>Blogcollab</h1>
<h2>What is this thing?</h2>

<p>Blogcollab is a really simple, git-backed collaboration tool for bloggers
that want to run their posts by trusted colleagues before pressing the
"publish" button.</p>

<p>The idea is that users can set up their own directories where they can post
new entries with git, so all content is version-controlled. The posts are even
easily styled with a simple template file, and the whole system can be
protected with straightforward HTTP authentication.</p>

<p>To get started, <a href="./?dologin=1">log in</a> with the default username
and password (collab/collab), and browse to the example content. (If you're
seeing this page, you're not logged in.)</p>
